"""
Phase 3 — E2E benchmark for top Phase 2 models (slim/ollama mode)

Reads Phase 2 passing models from eval/results/phase2/_phase2_summary.json
and runs e2e_benchmark --provider ollama --slim for each.

Usage:
  OLLAMA_HOST=http://localhost:11435 python eval/run_phase3.py
  OLLAMA_HOST=http://localhost:11435 python eval/run_phase3.py --resume
  OLLAMA_HOST=http://localhost:11435 python eval/run_phase3.py --models exaone3.5:7.8b
"""
from __future__ import annotations

import argparse
import asyncio
import json
import os
import sys
import time
from pathlib import Path

ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(ROOT))

PHASE2_SUMMARY = ROOT / "eval" / "results" / "phase2" / "_phase2_summary.json"
RESULTS_DIR    = ROOT / "eval" / "results" / "phase3"

# Phase 3 pass gate (production readiness)
PASS_GATE = {
    "avg_overall_score": 0.50,   # e2e report quality
    "avg_revision_count": 3.0,   # AlignRAG loops ≤ 3 (lower is better)
}


def _safe_model_name(model: str) -> str:
    return model.replace(":", "_").replace(".", "_")


def _load_phase2_candidates() -> list[str]:
    if not PHASE2_SUMMARY.exists():
        # Fallback: use Phase 1 candidates directly if Phase 2 has no passing models
        p1 = ROOT / "eval" / "results" / "phase1" / "_phase1_summary.json"
        if p1.exists():
            data = json.loads(p1.read_text())
            candidates = data.get("phase2_candidates", [])
            print(f"  (Phase 2 summary not found — using Phase 1 candidates: {candidates})")
            return candidates
        print("No phase summary found. Specify --models explicitly.")
        sys.exit(1)
    data = json.loads(PHASE2_SUMMARY.read_text())
    candidates = data.get("phase3_candidates", [])
    if not candidates:
        # If none passed gate, take top-2 by avg_score_on
        models = sorted(data.get("models", []),
                        key=lambda x: x.get("avg_score_on", 0), reverse=True)
        candidates = [m["model"] for m in models[:2] if "error" not in m]
        print(f"  (No Phase 2 passed — using top-2 by score: {candidates})")
    return candidates


async def run_one_model(model_name: str) -> dict:
    """Run e2e_benchmark slim conditions for one model."""
    host = os.getenv("OLLAMA_HOST", "http://localhost:11435")
    os.environ["OLLAMA_HOST"] = host
    os.environ["OLLAMA_MODEL"] = model_name

    print(f"\n{'#' * 70}")
    print(f"  PHASE 3 — MODEL: {model_name}")
    print(f"  HOST: {host}")
    print(f"  Conditions: baseline, phase1_2, phase1_2_3")
    print(f"{'#' * 70}")

    import subprocess
    result_file = RESULTS_DIR / f"{_safe_model_name(model_name)}_e2e.json"

    t0 = time.time()
    proc = await asyncio.create_subprocess_exec(
        sys.executable, "-m", "eval.e2e_benchmark",
        "--provider", "ollama",
        "--model", model_name,
        "--label", f"phase3_{_safe_model_name(model_name)}",
        "--slim",
        "--conditions", "baseline,phase1_2,phase1_2_3",
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.STDOUT,
        env={**os.environ, "OLLAMA_HOST": host, "OLLAMA_MODEL": model_name},
        cwd=str(ROOT),
    )
    stdout, _ = await proc.communicate()
    elapsed = round(time.time() - t0, 1)
    output = stdout.decode("utf-8", errors="replace")
    print(output)

    if proc.returncode != 0:
        return {
            "model": model_name,
            "error": f"exit code {proc.returncode}",
            "output_tail": output[-500:],
            "passed": False,
        }

    # Find the output JSON file generated by e2e_benchmark
    label = f"phase3_{_safe_model_name(model_name)}"
    e2e_result_path = ROOT / "eval" / "results" / f"{label}.json"

    if not e2e_result_path.exists():
        # Try to find by glob
        candidates = list((ROOT / "eval" / "results").glob(f"*{_safe_model_name(model_name)}*.json"))
        if candidates:
            e2e_result_path = max(candidates, key=lambda p: p.stat().st_mtime)
        else:
            return {
                "model": model_name,
                "error": "result file not found",
                "passed": False,
            }

    data = json.loads(e2e_result_path.read_text())
    conditions = {c["name"]: c["aggregate"] for c in data.get("conditions", [])}

    # Extract key metrics from phase1_2_3 condition (most complete)
    best_cond = conditions.get("phase1_2_3", conditions.get("phase1_2", {}))
    avg_overall = best_cond.get("avg_overall_score", 0)
    avg_revision = best_cond.get("avg_revision_count", 99)
    latency = best_cond.get("latency_sec", 0)

    passed = (
        avg_overall >= PASS_GATE["avg_overall_score"]
        and avg_revision <= PASS_GATE["avg_revision_count"]
    )
    verdict = "✅ PASS" if passed else "❌ FAIL"
    print(f"\n  Phase 3 verdict: {verdict}")
    print(f"  avg_overall_score={avg_overall:.3f} (gate≥{PASS_GATE['avg_overall_score']})")
    print(f"  avg_revision_count={avg_revision:.2f} (gate≤{PASS_GATE['avg_revision_count']})")
    print(f"  latency={latency:.1f}s/query")

    summary = {
        "model": model_name,
        "elapsed_sec": elapsed,
        "best_condition": "phase1_2_3",
        "avg_overall_score": avg_overall,
        "avg_revision_count": avg_revision,
        "latency_sec": latency,
        "passed": passed,
        "conditions": conditions,
        "e2e_result_file": str(e2e_result_path),
    }
    result_file.write_text(json.dumps(summary, indent=2))
    print(f"\n  Saved: {result_file}")
    return summary


async def main(args: argparse.Namespace) -> None:
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    if args.models:
        candidates = [m.strip() for m in args.models.split(",")]
    else:
        candidates = _load_phase2_candidates()

    print(f"\nPhase 3 candidates: {candidates}")

    all_summaries: list[dict] = []

    for model_name in candidates:
        result_path = RESULTS_DIR / f"{_safe_model_name(model_name)}_e2e.json"

        if args.resume and result_path.exists():
            print(f"\n  [SKIP] {model_name} — result already exists")
            summary = json.loads(result_path.read_text())
            all_summaries.append(summary)
            continue

        summary = await run_one_model(model_name)
        all_summaries.append(summary)

    # ── Final leaderboard ──────────────────────────────────────────────────────
    print(f"\n\n{'═' * 70}")
    print("  PHASE 3 LEADERBOARD — FINAL RANKING")
    print(f"{'═' * 70}")
    print(f"  {'Model':<22} {'Quality':<10} {'Revisions':<11} {'Latency':<10} {'Result'}")
    print(f"  {'─'*22} {'─'*10} {'─'*11} {'─'*10} {'─'*10}")

    final_picks = []
    for s in sorted(all_summaries, key=lambda x: x.get("avg_overall_score", 0), reverse=True):
        if "error" in s:
            print(f"  {s['model']:<22} ERROR: {s['error'][:40]}")
            continue
        verdict = "✅ PASS" if s["passed"] else "❌ FAIL"
        print(f"  {s['model']:<22} {s['avg_overall_score']:.3f}     "
              f"{s['avg_revision_count']:.2f}       "
              f"{s['latency_sec']:.0f}s        {verdict}")
        if s["passed"]:
            final_picks.append(s["model"])

    if final_picks:
        print(f"\n  RECOMMENDATION: {final_picks[0]}")
    else:
        best = sorted(all_summaries, key=lambda x: x.get("avg_overall_score", 0), reverse=True)
        if best and "error" not in best[0]:
            print(f"\n  BEST (no gate passed): {best[0]['model']} "
                  f"(score={best[0].get('avg_overall_score', 0):.3f})")

    combined_path = RESULTS_DIR / "_phase3_summary.json"
    combined_path.write_text(json.dumps({
        "models": all_summaries,
        "pass_gate": PASS_GATE,
        "final_recommendations": final_picks,
    }, indent=2))
    print(f"\n  Combined results: {combined_path}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Phase 3 E2E benchmark runner")
    parser.add_argument("--models", default=None)
    parser.add_argument("--resume", action="store_true")
    args = parser.parse_args()
    asyncio.run(main(args))
